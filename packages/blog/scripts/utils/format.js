/**
 * 与えられた文字列内の `tags: '[...]'` の部分のシングルクォーテーションを削除します。
 * @param {string} text - 処理対象の文字列
 * @returns {string} フォーマットされた文字列
 */
export function removeTagsQuotes(text) {
  // 正規表現を使用して `tags: '...'` のパターンを検索します。
  // ここでは、`tags: ` の後に続くシングルクォーテーションで囲まれた任意の文字 (`.`) が
  // 複数回 (`+`) 続くパターンを `g` (グローバル) フラグで全体に適用します。
  const regex = /tags: '(.+)'/;

  // `replace` メソッドを使用して、マッチした部分を置換します。
  // `$1` は正規表現の括弧 `(...)` でキャプチャされた部分（シングルクォーテーションの中身）を指します。
  return text.replace(regex, "tags: $1");
}
